Timeline.prototype.defineStyle = function() {
  const timeline = this;
  const style = {};

  style.backgroundColor = "white";

  style.axisLineColor = "#aaa";
  style.axisFontSize = "9pt";
  style.axisFontWeight = "normal";
  style.axisFontColor = "black";
  style.axisDashColor = "#ccc";

  return style;
}
